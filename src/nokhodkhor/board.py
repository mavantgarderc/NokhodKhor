def make_sparse_variant(base: List[List[int]], step: int) -> List[List[int]]:
    level = copy.deepcopy(base)
    for i, row in enumerate(level):
        for j, val in enumerate(row):
            if val == 1:
                if (i + j) % step == 0:
                    level[i][j] = 0
    return level


def make_dense_variant(base: List[List[int]], step: int) -> List[List[int]]:
    level = copy.deepcopy(base)
    for i, row in enumerate(level):
        for j, val in enumerate(row):
            if val == 0:
                if 2 <= i <= len(level) - 3 and 2 <= j <= len(row) - 3:
                    if (i * j) % step == 0:
                        level[i][j] = 1
    return level


LEVEL_1 = BASE_LEVEL
LEVEL_2 = make_sparse_variant(BASE_LEVEL, step=2)
LEVEL_3 = make_dense_variant(BASE_LEVEL, step=7)


boards: List[List[List[int]]] = [LEVEL_1, LEVEL_2, LEVEL_3]


def draw_board(screen: pygame.Surface, level: List[List[int]], flicker: bool) -> None:
    num1 = (HEIGHT - 50) // 32
    num2 = WIDTH // 30

    for i, row in enumerate(level):
        for j, cell in enumerate(row):
            x_center = j * num2 + (0.5 * num2)
            y_center = i * num1 + (0.5 * num1)

            if cell == 1:
                pygame.draw.circle(screen, "white", (x_center, y_center), 4)

            if cell == 2 and not flicker:
                pygame.draw.circle(screen, "white", (x_center, y_center), 10)

            if cell == 3:
                pygame.draw.line(
                    screen,
                    COLOR_WALL,
                    (j * num2 + (0.5 * num2), i * num1),
                    (j * num2 + (0.5 * num2), i * num1 + num1),
                    3,
                )
            if cell == 4:
                pygame.draw.line(
                    screen,
                    COLOR_WALL,
                    (j * num2, i * num1 + (0.5 * num1)),
                    (j * num2 + num2, i * num1 + (0.5 * num1)),
                    3,
                )
            if cell == 5:
                pygame.draw.arc(
                    screen,
                    COLOR_WALL,
                    [
                        (j * num2 - (num2 * 0.4)) - 2,
                        (i * num1 + (0.5 * num1)),
                        num2,
                        num1,
                    ],
                    0,
                    PI / 2,
                    3,
                )
            if cell == 6:
                pygame.draw.arc(
                    screen,
                    COLOR_WALL,
                    [
                        (j * num2 + (num2 * 0.5)),
                        (i * num1 + (0.5 * num1)),
                        num2,
                        num1,
                    ],
                    PI / 2,
                    PI,
                    3,
                )
            if cell == 7:
                pygame.draw.arc(
                    screen,
                    COLOR_WALL,
                    [
                        (j * num2 + (num2 * 0.5)),
                        (i * num1 - (0.4 * num1)),
                        num2,
                        num1,
                    ],
                    PI,
                    3 * PI / 2,
                    3,
                )
            if cell == 8:
                pygame.draw.arc(
                    screen,
                    COLOR_WALL,
                    [
                        (j * num2 - (num2 * 0.4)) - 2,
                        (i * num1 - (0.4 * num1)),
                        num2,
                        num1,
                    ],
                    3 * PI / 2,
                    2 * PI,
                    3,
                )
            if cell == 9:
                pygame.draw.line(
                    screen,
                    "white",
                    (j * num2, i * num1 + (0.5 * num1)),
                    (j * num2 + num2, i * num1 + (0.5 * num1)),
                    3,
                )
